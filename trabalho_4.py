
# Exemplo de código Python com estrutura de repetição e tomada de decisão

# Estrutura de repetição: Loop for
# Este código imprime os números de 1 a 10
for i in range(1, 11):
    print(i)

# Estrutura de tomada de decisão: if-elif-else
# Este código verifica se um número é positivo, negativo ou zero
numero = 5

if numero > 0:
    print("O número é positivo.")
elif numero < 0:
    print("O número é negativo.")
else:
    print("O número é zero.")
